# 12. Write a program to find the area of trapezium.
a=float(input("Enter the length of First Parallel Side of Trapezium : "))
b=float(input("Enter the length of Second Parallel Side of Trapezium : "))
h=float(input("Enter the height of Trapezium : "))
area=((a+b)*h)/2
print("Area of the Trapezium : ",area)